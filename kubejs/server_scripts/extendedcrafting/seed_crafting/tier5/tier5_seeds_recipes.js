function seedcraftingtier5(event, input) {
  event
    .custom({
      type: 'extendedcrafting:shaped_table',
      pattern: [
        'ABBBBBA',
        'BCCCCCB',
        'BCDDDCB',
        'BCDEDCB',
        'BCDDDCB',
        'BCCCCCB',
        'ABBBBBA',
      ],
      key: {
        A: {
          item: 'mysticalagriculture:supremium_essence',
        },
        B: {
          item: 'extendedcrafting:enhanced_ender_ingot_block',
        },
        C: {
          item: 'extendedcrafting:black_iron_block',
        },
        D: {
          item: input[1],
        },
        E: {
          item: 'kubejs:tier5_crafting_seed',
        },
      },
      result: {
        item: input[0],
      },
    })
    .id('extended_' + input[0]);
}
